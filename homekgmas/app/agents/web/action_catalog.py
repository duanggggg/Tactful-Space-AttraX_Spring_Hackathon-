"""Normalized action catalog for the standalone web simulator."""

from __future__ import annotations

from typing import Any

from pydantic import BaseModel, Field


class WebActionSpec(BaseModel):
    """One normalized web action that can be rendered directly in the UI."""

    action_key: str
    agent_name: str
    room: str
    service: str
    label: str
    description: str
    params: dict[str, Any] = Field(default_factory=dict)
    env_targets: list[str] = Field(default_factory=list)


def _room_targets(room: str, *variables: str) -> list[str]:
    return [f"{room}_{variable}" for variable in variables]


def _spec(
    *,
    action_key: str,
    agent_name: str,
    room: str,
    service: str,
    label: str,
    description: str,
    params: dict[str, Any],
    env_targets: list[str],
) -> WebActionSpec:
    return WebActionSpec(
        action_key=action_key,
        agent_name=agent_name,
        room=room,
        service=service,
        label=label,
        description=description,
        params=params,
        env_targets=env_targets,
    )


