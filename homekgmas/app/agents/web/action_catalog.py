WEB_ACTION_INDEX: dict[str, WebActionSpec] = {
    spec.action_key: spec for spec in WEB_ACTION_SPECS
}


def list_web_actions() -> list[WebActionSpec]:
    """Return the full web action catalog."""

    return list(WEB_ACTION_SPECS)


def get_web_action(action_key: str) -> WebActionSpec:
    """Return one action spec by key."""

    try:
        return WEB_ACTION_INDEX[action_key]
    except KeyError as exc:
        raise KeyError(f"Unknown web action: {action_key}") from exc
